    CONSTRAINT pkNota PRIMARY KEY (idNota),
    CONSTRAINT fkNotaUsuario FOREIGN KEY (idUsuario) REFERENCES Usuarios(idUsuario),
    CONSTRAINT fkNotaCategoriaNota FOREIGN KEY (idCategoriaNota) REFERENCES CategoriasNota(idCategoriaNota),
    CONSTRAINT ckActivoNota CHECK (activo IN (TRUE, FALSE))
);

