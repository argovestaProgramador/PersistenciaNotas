CREATE OR REPLACE FUNCTION fn_listarRolesUsuario()
RETURNS TABLE (
    idRol INT,
    nombre VARCHAR(50),
    descripcion VARCHAR(250)
)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY 
    SELECT rol.idRolUsuario AS "idRolUsuario",
           rol.nombre AS "nombre",
           rol.descripcion AS "descripcion"
    FROM RolesUsuario AS rol
    WHERE rol.activo = TRUE;
END;
$$;

CREATE OR REPLACE PROCEDURE sp_crearRolUsuario(
    p_nombre VARCHAR(50),
    p_descripcion VARCHAR(250)
)
LANGUAGE plpgsql
AS $$
BEGIN
    INSERT INTO RolesUsuario(nombre, descripcion, activo, fechaCreacionRegistro)
    VALUES (p_nombre, p_descripcion, TRUE, CURRENT_DATE);
    COMMIT;
END;
$$;

CREATE OR REPLACE PROCEDURE sp_actualizarRolUsuario(
    p_nombre VARCHAR(50),
    p_descripcion VARCHAR(250),
    p_idRolUsuario INT
)
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE RolesUsuario
    SET nombre = p_nombre, descripcion = p_descripcion
    WHERE idRolUsuario = p_idRolUsuario;
    COMMIT;
END;
$$;

CREATE OR REPLACE PROCEDURE sp_eliminarRolUsuario(
    p_idRolUsuario INT
)
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE RolesUsuario
    SET activo = FALSE
    WHERE idRolUsuario = p_idRolUsuario;
    COMMIT;
END;
$$;

